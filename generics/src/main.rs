// defining function using generics
// Need to restrict T to only those types who support comparisons
// that can be restricted using PartialOrd for generic
// as ch and i32 both have implemented PartialOrd Trait
fn largest<T: PartialOrd>(list: &[T]) -> &T {
    let mut largest = &list[0];

    for item in list {
        if item > largest {
            largest = item;
        }
    }

    largest
}
